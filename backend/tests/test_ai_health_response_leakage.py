        db, user_a, _ = _make_db_and_user("p37a@example.com")

        user_b = User(
            id=uuid.uuid4(),
            email="p37b@example.com",
            password_hash="hashed_not_real",
            is_active=True,
        )
        db.add(user_b)
        db.flush()
        person_b = PersonProfile(
            id=uuid.uuid4(),
            owner_user_id=user_b.id,
            display_name="User B",
            relationship="self",
            is_default=True,
        )
        db.add(person_b)
        db.commit()

        # user_a authenticates but targets user_b's person_id
        client = _make_client(db, user_a)
        resp = client.get("/api/v1/ai-summary", params={"person_id": str(person_b.id)})
        assert resp.status_code == 404, (
            f"Expected 404 for foreign person_id on GET /ai-summary, got {resp.status_code}"
        )

