class TestReportStatusAuthHardened:
    """GET /api/v1/reports/{report_id} must enforce ownership.

    user_a generates a report.  user_b must not be able to query its status
    or receive the download URL.
    """

    def _generate_as_user_a(self) -> tuple[Session, User, User, str, str]:
        """Generate a report as user_a; return (db, user_a, user_b, report_id, token)."""
        db, user_a, _person_a, user_b, _person_b = _build_two_user_db()
        client_a = _set_user(db, user_a)

        resp = client_a.post('/api/v1/reports/generate', json={})
        assert resp.status_code == 202, f"Generate failed: {resp.text}"
        report_id = resp.json()['report_id']
        token = _REPORT_STATE[report_id]['token']
        return db, user_a, user_b, report_id, token

    def test_status_own_report_ok(self):
        db, user_a, _user_b, report_id, _token = self._generate_as_user_a()
        client_a = _set_user(db, user_a)

        resp = client_a.get(f'/api/v1/reports/{report_id}')
        assert resp.status_code == 200, f"Owner should see own report, got {resp.status_code}: {resp.text}"
        data = resp.json()
        assert data['status'] == 'ready', f"Expected ready, got {data['status']}"
        assert data.get('download_url') is not None, "Owner should receive download_url"

    def test_status_cross_user_denied(self):
        db, user_a, user_b, report_id, _token = self._generate_as_user_a()
        client_b = _set_user(db, user_b)

        resp = client_b.get(f'/api/v1/reports/{report_id}')
        assert resp.status_code == 404, (
            f"Cross-user report status: expected 404, got {resp.status_code}. Body: {resp.text}"
        )

    def test_status_unknown_report_denied(self):
        db, user_a, _user_b, _report_id, _token = self._generate_as_user_a()
        client_a = _set_user(db, user_a)

        fake_id = str(uuid.uuid4())
        resp = client_a.get(f'/api/v1/reports/{fake_id}')
        assert resp.status_code == 404, (
            f"Unknown report_id: expected 404, got {resp.status_code}. Body: {resp.text}"
        )

    def test_status_response_no_leak(self):
        """The 404 response body must not contain user_a's report_id or token."""
        db, user_a, user_b, report_id, token = self._generate_as_user_a()
        client_b = _set_user(db, user_b)

        resp = client_b.get(f'/api/v1/reports/{report_id}')
        assert resp.status_code == 404
        body = resp.text
        assert report_id not in body, "404 body must not echo the report_id"
        assert token not in body, "404 body must not leak the download token"
        assert str(user_a.id) not in body, "404 body must not leak user_a.id"


