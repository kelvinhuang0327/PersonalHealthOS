class TestReportDownloadTokenOnly:
    """GET /api/v1/reports/download/{report_id}?token=...

    The download endpoint is token-only (no JWT auth).  The token is a UUID
    secret only obtainable via the hardened status endpoint (which now denies
    cross-user access).  Tests here verify the token-validation logic.

    Known gap: if the token leaks via other means (browser history, network
    capture), a third party can download.  This is the documented DOWNLOAD_GAP
    and is acceptable given the 1-hour expiry and unguessable UUID token.
    """

    def _generate_as_user_a(self) -> tuple[Session, User, User, str, str]:
        db, user_a, _person_a, user_b, _person_b = _build_two_user_db()
        client_a = _set_user(db, user_a)

        resp = client_a.post('/api/v1/reports/generate', json={})
        assert resp.status_code == 202
        report_id = resp.json()['report_id']
        token = _REPORT_STATE[report_id]['token']
        return db, user_a, user_b, report_id, token

    def test_download_valid_token_ok(self):
        """Owner uses the token obtained via status → download succeeds."""
        db, user_a, _user_b, report_id, token = self._generate_as_user_a()
        # download endpoint has no JWT: token is the sole credential
        client_a = _set_user(db, user_a)

        resp = client_a.get(f'/api/v1/reports/download/{report_id}', params={'token': token})
        assert resp.status_code == 200, (
            f"Valid token download failed: {resp.status_code}: {resp.text}"
        )
        assert resp.headers.get('content-type', '').startswith('application/pdf'), (
            f"Expected PDF content-type, got {resp.headers.get('content-type')}"
        )

    def test_download_wrong_token_denied(self):
        """Correct report_id but wrong token → 403."""
        db, user_a, _user_b, report_id, _token = self._generate_as_user_a()
        client_a = _set_user(db, user_a)

        wrong_token = str(uuid.uuid4())
        resp = client_a.get(f'/api/v1/reports/download/{report_id}', params={'token': wrong_token})
        assert resp.status_code == 403, (
            f"Wrong token: expected 403, got {resp.status_code}. Body: {resp.text}"
        )

    def test_download_unknown_report_denied(self):
        """Unknown report_id → 404."""
        db, user_a, _user_b, _report_id, _token = self._generate_as_user_a()
        client_a = _set_user(db, user_a)

        fake_id = str(uuid.uuid4())
        resp = client_a.get(f'/api/v1/reports/download/{fake_id}', params={'token': 'any'})
        assert resp.status_code == 404, (
            f"Unknown report_id: expected 404, got {resp.status_code}. Body: {resp.text}"
        )
