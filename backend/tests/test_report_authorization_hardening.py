@pytest.fixture(autouse=True)
def _clear_app_overrides_and_report_state():
    """Isolate each test: wipe dependency overrides + in-memory report state."""
    _REPORT_STATE.clear()
    yield
    app.dependency_overrides.clear()
    _REPORT_STATE.clear()


def _build_two_user_db() -> tuple[Session, User, PersonProfile, User, PersonProfile]:
    """Create two users each owning one PersonProfile in a fresh SQLite DB.

    Returns (db, user_a, person_a, user_b, person_b).
    """
    engine = create_engine(
        "sqlite://",
        connect_args={"check_same_thread": False},
        poolclass=StaticPool,
    )
    SLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)
    Base.metadata.create_all(bind=engine)
    db: Session = SLocal()

    user_a = User(email=f"p18_a_{uuid.uuid4().hex[:8]}@example.com", password_hash="h")
    user_b = User(email=f"p18_b_{uuid.uuid4().hex[:8]}@example.com", password_hash="h")
    db.add_all([user_a, user_b])
    db.commit()
    db.refresh(user_a)
    db.refresh(user_b)

    person_a = PersonProfile(
        owner_user_id=user_a.id,
        display_name="P18 User A Self",
        relationship="self",
        is_default=True,
    )
    person_b = PersonProfile(
        owner_user_id=user_b.id,
        display_name="P18 User B Self",
        relationship="self",
        is_default=True,
    )
    db.add_all([person_a, person_b])
    db.commit()
    db.refresh(person_a)
    db.refresh(person_b)

    return db, user_a, person_a, user_b, person_b


def _set_user(db: Session, user: User) -> TestClient:
    """Switch dependency overrides to the given user and return a TestClient."""
    def _override_db():
        yield db

    app.dependency_overrides[get_db] = _override_db
    app.dependency_overrides[get_current_user] = lambda: user
    return TestClient(app)


# ---------------------------------------------------------------------------
# TestReportGenerateOwnerBinding
# ---------------------------------------------------------------------------


class TestReportGenerateOwnerBinding:
    """Verifies that generate_report stores owner_user_id in _REPORT_STATE."""

    def test_generate_sets_owner_user_id(self):
        db, user_a, person_a, _user_b, _person_b = _build_two_user_db()
        client = _set_user(db, user_a)

        resp = client.post('/api/v1/reports/generate', json={})
        assert resp.status_code == 202, f"Expected 202, got {resp.status_code}: {resp.text}"

        report_id = resp.json()['report_id']
        state = _REPORT_STATE.get(report_id)
        assert state is not None, "Report state not found in _REPORT_STATE"
        assert 'owner_user_id' in state, "_REPORT_STATE entry missing owner_user_id"
        assert state['owner_user_id'] == str(user_a.id), (
            f"Expected owner_user_id={user_a.id}, got {state['owner_user_id']}"
        )


