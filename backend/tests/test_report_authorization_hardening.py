"""P18 — Report Authorization Hardening Regression

Verifies that the report status endpoint enforces report ownership so that
user B cannot query or receive the download URL for a report generated by
user A.

Mechanism under test
--------------------
``_REPORT_STATE`` (in-memory dict, app/api/reports.py) now stores
``owner_user_id = str(current_user.id)`` for every generated report.

``GET /api/v1/reports/{report_id}`` (get_report_status) now checks:
    str(state['owner_user_id']) != str(current_user.id)  →  HTTP 404

``GET /api/v1/reports/download/{report_id}?token=...`` remains token-only
(no JWT auth) — this is the documented DOWNLOAD_GAP.  The token is only
ever returned to the report owner through the hardened status endpoint, so
in practice the download URL is not obtainable by another user without the
status endpoint leaking it.

Coverage
--------
TestReportStatusAuthHardened
  test_status_own_report_ok               → 200, status ready
  test_status_cross_user_denied           → 404 (no data leak)
  test_status_unknown_report_denied       → 404
  test_status_response_no_leak            → body must not contain owner's id

TestReportGenerateOwnerBinding
  test_generate_sets_owner_user_id        → _REPORT_STATE contains owner_user_id

TestReportDownloadTokenOnly
  test_download_valid_token_ok            → 200 (owner uses correct token)
  test_download_wrong_token_denied        → 403
  test_download_unknown_report_denied     → 404

Strategy: SQLite in-memory DB + get_db override + get_current_user override.
get_target_person is NOT overridden — production ownership code runs unchanged.
_REPORT_STATE is cleared before and after each test to prevent state leakage.
"""
from __future__ import annotations

import uuid

import pytest
from fastapi.testclient import TestClient
from sqlalchemy import create_engine
from sqlalchemy.orm import Session, sessionmaker
from sqlalchemy.pool import StaticPool

from app.api.reports import _REPORT_STATE
from app.core.database import Base, get_db
from app.core.deps import get_current_user
from app.main import app
from app.models.entities import PersonProfile, User


